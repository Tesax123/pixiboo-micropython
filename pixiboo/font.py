def get_char_pattern(char: str):
    """
    Get the 7x7 pattern for a character.
    
    Args:
        char: Single character (will be converted to uppercase)
    
    Returns:
        List of 7 strings representing the character pattern, or None if not found
    """
    char_upper = char.upper()
    pattern = FONT_7x7.get(char_upper)
    
    # Return empty pattern for unknown characters
    if pattern is None:
        return ["0000000"] * 7
    
    return pattern


